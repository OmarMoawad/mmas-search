# ---- helper: fastest algo per dataset ----
def fastest(ds):
    best, ba = 1e30, None
    for a in ALGOS:
        v = ns(ds, a)
        if v is not None and v < best: best, ba = v, a
    return ba, best

# ---- Abstract ----
H("Abstract", 1)
P("Model-based (interpolation-style) search predicts a key's position from a fitted "
  "model of the key distribution and verifies with a probe, instead of repeatedly halving "
  "an interval. On smooth data it can use far fewer array accesses than binary search; on "
  "adversarial data it degrades to O(n) unless capped. We describe Multi-Model Adaptive "
  "Search (MMAS), a training-free, closed-form realization: it detects a global "
  "linear/power/exponential model from five samples, inverts it to jump to the predicted "
  "index, and falls back to binary search, with a worst case of 8 + 2⌈log2 n⌉ probes. "
  "We then add the extension that makes the idea useful on data fitting no global "
  "closed-form curve: a piecewise-linear (PGM-style) model built once per array.")
P("Critically, we correct the evaluation of the first version. Probe count is the right "
  "cost metric ONLY when a probe is expensive (a cache miss, an SSD read, a network "
  "round-trip). We therefore report both measured in-RAM wall-clock time and probe counts, "
  "on synthetic curves and two REAL datasets harvested from a filesystem, with 50% of "
  "queries absent. Three honest findings emerge. (1) Plain interpolation search is "
  "DANGEROUS on real data: up to ~4,000x slower than binary on heavy-tailed file sizes and "
  "~9,000x on exponential data, because non-uniform spacing makes its position estimate "
  "wildly wrong. (2) The closed-form MMAS models never fit our real data, so MMAS "
  "correctly falls back to binary search — neither helping nor hurting; its single-probe "
  "wins are confined to synthetic arrays generated by the exact law it inverts. (3) The "
  "piecewise-linear index (pgm_pla) is the one method that helps on real data: it beats "
  "binary on in-RAM wall-clock on the larger real dataset and on adversarial data, and "
  "dominates decisively once probes are expensive. Binary search remains the robust "
  "default — unbeatable on small arrays and never catastrophic. MMAS is best understood "
  "not as a “binary-search killer” but as a deployment-friendly, closed-form baseline for "
  "the learned-index family; its piecewise variant is where the real, general win lives.")

