"""Build the honest reframed paper (.docx) with numbers auto-filled from results.csv."""
import csv, sys, collections
from docx import Document
from docx.shared import Pt, RGBColor, Inches
from docx.enum.text import WD_ALIGN_PARAGRAPH

CSV = sys.argv[1] if len(sys.argv) > 1 else "results.csv"
OUT = sys.argv[2] if len(sys.argv) > 2 else "mmas_paper_honest.docx"

rows = list(csv.DictReader(open(CSV)))
D = collections.OrderedDict()
for r in rows:
    D.setdefault(r["dataset"], collections.OrderedDict())[r["algo"]] = r

def fnum(x):
    try: return float(x)
    except: return None

def g(ds, algo, col):
    return D.get(ds, {}).get(algo, {}).get(col, "")

def ns(ds, algo):
    return fnum(g(ds, algo, "ns_per_query"))

def tot_probes(ds, algo):
    d = fnum(g(ds, algo, "mean_data_probes")) or 0
    m = fnum(g(ds, algo, "mean_model_probes")) or 0
    return d + m

ALGOS = ["stl_lb", "binary", "interp", "mmas_closed", "pgm_pla"]
NICE = {"stl_lb":"std::lower_bound","binary":"binary","interp":"interpolation",
        "mmas_closed":"mmas_closed","pgm_pla":"pgm_pla (piecewise)"}
SYN = [d for d in D if d.startswith("syn_")]
REAL = [d for d in D if d.startswith("real_")]

doc = Document()
st = doc.styles["Normal"]; st.font.name = "Calibri"; st.font.size = Pt(10.5)

def H(text, lvl=1):
    h = doc.add_heading(text, level=lvl)
    return h

def P(text, italic=False, bold=False):
    p = doc.add_paragraph()
    r = p.add_run(text); r.italic = italic; r.bold = bold
    return p

def bullets(items):
    for it in items:
        doc.add_paragraph(it, style="List Bullet")

