def add_ram_table(datasets, caption):
    P(caption, bold=True)
    tbl = doc.add_table(rows=1, cols=7); tbl.style = "Light Grid Accent 1"
    hdr = ["dataset","algo","ns/query","x stl_lb","mean probes","max probes","detected"]
    for i,h in enumerate(hdr): tbl.rows[0].cells[i].text = h
    for ds in datasets:
        lb = ns(ds,"stl_lb")
        for a in ALGOS:
            if a not in D[ds]: continue
            row = tbl.add_row().cells
            v = ns(ds,a)
            row[0].text = ds if a==ALGOS[0] else ""
            row[1].text = NICE[a]
            row[2].text = f"{v:.1f}" if v is not None else ""
            row[3].text = (f"{v/lb:.2f}x" if (v and lb) else "")
            dp = fnum(g(ds,a,"mean_data_probes")); mp = g(ds,a,"max_data_probes")
            row[4].text = f"{dp:.2f}" if dp is not None else ""
            row[5].text = mp or ""
            row[6].text = g(ds,a,"detected")
add_ram_table(SYN, "Synthetic (n = 1,000,000):")
add_ram_table(REAL, "Real data:")

