 "data — and its lead widens as probes get expensive. This is exactly why learned indexes "
 "matter for on-disk / distributed stores.",
 "Binary search is the robust default: never catastrophic, and fastest on small arrays "
 "where model overhead is not amortized. It is the baseline to respect, not defeat.",
 "None of this breaks the information-theoretic lower bound; the advantage, when present, "
